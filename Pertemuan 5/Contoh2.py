buah = {"nangka", "jambu", "pisang"}

# tuple
data_buah = ("nangka", "jambu", "pisang")
print(type(data_buah))

print(data_buah[0])
print(data_buah[1])
print(data_buah[2])

# list
data_ardan = ["ardan", "sangat", "imut"]
print(type(data_ardan))

print(data_ardan[0])
print(data_ardan[1])
print(data_ardan[2])

# dictionary
data_pribadi = {
    "nama": "ardan",
    "asal": "mars",
}
print(type(data_pribadi))

print(data_pribadi["nama"])
print(data_pribadi["asal"])
